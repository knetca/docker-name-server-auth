#!/bin/sh
# deploy-zones.sh — poll knet-zones git repo, deploy on change, reload NSD
#
# On each cron invocation:
#   1. Clone repo if not present, otherwise fetch and fast-forward
#   2. Compare HEAD before and after — exit early if no change
#   3. Copy *.zone from zones/ and zones_arpa/ to the shared zones volume
#   4. Generate /etc/nsd/nsd.conf.d/20-zones.conf from enumerated zone files
#   5. Validate NSD config
#   6. Reconfig and reload NSD (preserves cache, picks up new zones)
#
# nsd-control connects to 127.0.0.1:8952 (host networking, no TLS).
set -eu

ZONES_REPO="${ZONES_REPO:?}"
ZONES_BRANCH="${ZONES_BRANCH:-main}"
WORK_DIR="/var/lib/nsd-manager/zones"
ZONE_DEST="/etc/nsd/zones"
ZONES_CONF="/etc/nsd/nsd.conf.d/20-zones.conf"

log() { echo "$(date -Iseconds) [deploy-zones] $*"; }

# --- Clone or update ---
if [ ! -d "${WORK_DIR}/.git" ]; then
    log "Cloning ${ZONES_REPO} branch=${ZONES_BRANCH}"
    git clone --branch "${ZONES_BRANCH}" --single-branch \
        "${ZONES_REPO}" "${WORK_DIR}"
    CHANGED=1
else
    BEFORE=$(git -C "${WORK_DIR}" rev-parse HEAD)
    git -C "${WORK_DIR}" fetch --quiet origin "${ZONES_BRANCH}"
    git -C "${WORK_DIR}" reset --hard "origin/${ZONES_BRANCH}" --quiet
    AFTER=$(git -C "${WORK_DIR}" rev-parse HEAD)

    SHORT_BEFORE=$(echo "$BEFORE" | cut -c1-7)
    SHORT_AFTER=$(echo "$AFTER" | cut -c1-7)

    if [ "${BEFORE}" = "${AFTER}" ]; then
        log "No change: ${SHORT_BEFORE}"
        exit 0
    fi

    log "Updated: ${SHORT_BEFORE} → ${SHORT_AFTER}"
    CHANGED=1
fi

# --- Copy zone files ---
# Expects zone files in zones/ and zones_arpa/ subdirectories of the repo.
mkdir -p "${ZONE_DEST}"

DEPLOYED=0
for dir in zones zones_arpa; do
    SRC="${WORK_DIR}/${dir}"
    if [ ! -d "${SRC}" ]; then
        log "WARNING: ${dir}/ not found in repo — skipping"
        continue
    fi
    for f in "${SRC}"/*.zone; do
        [ -f "$f" ] || continue
        cp "$f" "${ZONE_DEST}/$(basename "$f")"
        DEPLOYED=$((DEPLOYED + 1))
    done
done

if [ "${DEPLOYED}" -eq 0 ]; then
    log "ERROR: No *.zone files found in zones/ or zones_arpa/"
    exit 1
fi

log "Deployed ${DEPLOYED} zone file(s)"

# --- Generate 20-zones.conf ---
# One zone: block per .zone file, zone name derived from filename.
# Health check zone is defined in 10-server.conf — excluded here.
ZONES_CONF_TMP="${ZONES_CONF}.tmp"

cat > "${ZONES_CONF_TMP}" <<EOF
# 20-zones.conf — generated by deploy-zones.sh
# Do not edit — changes will be overwritten on next deploy.
# Last updated: $(date -Iseconds)

EOF

for f in "${ZONE_DEST}"/*.zone; do
    [ -f "$f" ] || continue
    zonefile=$(basename "$f")
    zonename="${zonefile%.zone}"
    # Skip health check zone — managed in 10-server.conf
    [ "${zonename}" = "health.check.nsd" ] && continue
    cat >> "${ZONES_CONF_TMP}" <<EOF
zone:
    name: "${zonename}."
    zonefile: "${zonefile}"

EOF
    log "Zone: ${zonename}"
done

mv "${ZONES_CONF_TMP}" "${ZONES_CONF}"
log "Generated ${ZONES_CONF}"

# --- Validate ---
if ! nsd-checkconf /etc/nsd/nsd.conf >/dev/null 2>&1; then
    log "ERROR: nsd-checkconf failed — not reloading"
    exit 1
fi

# --- Reconfig and reload ---
if nsd-control -s 127.0.0.1@8952 status >/dev/null 2>&1; then
    nsd-control -s 127.0.0.1@8952 reconfig
    nsd-control -s 127.0.0.1@8952 reload
    log "NSD reconfig and reload complete"
else
    log "ERROR: nsd-control not reachable at 127.0.0.1:8952"
    exit 1
fi
